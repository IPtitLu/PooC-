using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace wowConsole
{
    class Arme
    {
        public int degatMin;
        public int degatMax;

        public Arme(int unDegatMin, int unDegatMax)
        {
            this.degatMin = unDegatMin;
            this.degatMax = unDegatMax;
        }

        public int getDegatMin()
        {
            return this.degatMax;
        }

        public int getDegatMax()
        {
            return this.degatMax;
        }
    }
}
